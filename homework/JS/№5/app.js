// 8
// ────────────────────────────────────────────────────────────
// Написать функцию, которая принимает часы, минуты и
// секунды и возвращает это время в секундах.

function returnSeconds(h = 0, m = 0, s = 0) {
    return (h * 60 * 60) + (m * 60) + s;
}

