// переводит их в часы, минуты и секунды и возвращает в
// виде строки «чч:мм:сс».

function returnTimeFromSeconds(s) {
    const h = (s / 3600) % 24,
        m = (s / 60) % 60,
        ns = s % 60;

    return `${h}:${m}:${ns}`
}

